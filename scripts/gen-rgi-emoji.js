#!/usr/bin/env node
// Generates src/quickjs/libunicode-rgi-emoji.h — the RGI_Emoji sequence
// tables used by libregexp for \p{RGI_Emoji} in unicodeSets (v-flag) mode.
//
// Usage: node scripts/gen-rgi-emoji.js /path/to/node_modules/@unicode/unicode-XX.0.0
//
// The @unicode/unicode-* npm packages (https://www.npmjs.com/org/unicode)
// publish the UTS #51 RGI_Emoji sequence property as a JS string array;
// the same data source test262's generated emoji tests are built from.
'use strict';

const fs = require('fs');
const path = require('path');

const dir = process.argv[2];
if (!dir) {
  console.error('usage: gen-rgi-emoji.js <path-to-@unicode/unicode-N.0.0>');
  process.exit(2);
}
const version = path.basename(dir).replace(/^unicode-/, '');
const strings = require(path.resolve(dir, 'Sequence_Property/RGI_Emoji/index.js'));

const singles = [];
const seqs = [];
for (const s of strings) {
  const cps = [...s].map((c) => c.codePointAt(0));
  if (cps.length === 1) singles.push(cps[0]);
  else seqs.push(cps);
}

seqs.sort((a, b) => {
  if (a.length !== b.length) return b.length - a.length;
  for (let i = 0; i < a.length; i++) if (a[i] !== b[i]) return a[i] - b[i];
  return 0;
});

singles.sort((a, b) => a - b);
const ranges = [];
for (const cp of singles) {
  if (ranges.length && cp === ranges[ranges.length - 1][1] + 1) ranges[ranges.length - 1][1] = cp;
  else if (ranges.length && cp <= ranges[ranges.length - 1][1]) continue;
  else ranges.push([cp, cp]);
}

const flat = [];
for (const cps of seqs) {
  flat.push(cps.length, ...cps);
}

const hex = (v) => '0x' + v.toString(16);
const wrap = (arr) => {
  const lines = [];
  for (let i = 0; i < arr.length; i += 8) lines.push('    ' + arr.slice(i, i + 8).join(', ') + ',');
  return lines.join('\n');
};

const out = `/* libunicode-rgi-emoji.h — RGI_Emoji (UTS #51) tables for \\p{RGI_Emoji}
 * in unicodeSets mode. Generated by scripts/gen-rgi-emoji.js from
 * @unicode/unicode-${version} — do not edit by hand.
 */

#ifndef LIBUNICODE_RGI_EMOJI_H
#define LIBUNICODE_RGI_EMOJI_H

#include <stdint.h>

#define LRE_RGI_EMOJI_SEQ_COUNT ${seqs.length}

static const uint32_t lre_rgi_emoji_seq[] = {
${wrap(flat.map(hex))}
};

#define LRE_RGI_EMOJI_RANGE_COUNT ${ranges.length}

/* CharRange points: [start, end) pairs, end exclusive */
static const uint32_t lre_rgi_emoji_single_ranges[] = {
${wrap(ranges.flatMap(([lo, hi]) => [lo, hi + 1]).map(hex))}
};

#endif
`;

fs.writeFileSync(path.join(__dirname, '..', 'src', 'quickjs', 'libunicode-rgi-emoji.h'), out);
console.log(`RGI_Emoji ${version}: ${seqs.length} sequences (max len ${seqs[0].length}), ${singles.length} single code points in ${ranges.length} ranges`);
